package com.logistimo.communication.core.utils;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import java.io.UnsupportedEncodingException;
import java.security.MessageDigest;
import java.security.NoSuchAlgorithmException;
import java.util.UUID;

/**
 * @see http://www.baeldung.com/java-uuid
 *
 */
public class ApiKeyGenerator {

  private static final char[] hexArray = "0123456789ABCDEF".toCharArray();

  private static final Logger log = LoggerFactory.getLogger(ApiKeyGenerator.class);

  private ApiKeyGenerator() {}

  public static String generateKey() {
    try {
      MessageDigest salt = MessageDigest.getInstance("SHA-256");
      salt.update(UUID.randomUUID().toString().getBytes("UTF-8"));
      return bytesToHex(salt.digest());
    } catch (NoSuchAlgorithmException | UnsupportedEncodingException ex) {
      log.warn("Issue with api key generation {}",ex.getMessage(),ex);
    }
    return null;
  }

  private static String bytesToHex(byte[] bytes) {
    char[] hexChars = new char[bytes.length * 2];
    for (int j = 0; j < bytes.length; j++) {
      int v = bytes[j] & 0xFF;
      hexChars[j * 2] = hexArray[v >>> 4];
      hexChars[j * 2 + 1] = hexArray[v & 0x0F];
    }
    return new String(hexChars);
  }
}
