package com.logistimo.communication.application.configs.condition;

import org.springframework.context.annotation.Condition;
import org.springframework.context.annotation.ConditionContext;
import org.springframework.core.type.AnnotatedTypeMetadata;

/**
 * Created by kumargaurav on 20/06/17.
 */
public class SentinelCondition implements Condition {

  @Override
  public boolean matches(ConditionContext context,
                         AnnotatedTypeMetadata annotatedTypeMetadata) {
    return context.getEnvironment().getProperty("app.issentinel").contains("true");
  }
}
